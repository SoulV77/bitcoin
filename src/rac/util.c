// integer 2-log (for numbers>1023)
int ilog2(uint32_t l) {
  int p = 0;
  if (l == 0) return 0;
  if (l & 0xFFF00000) {
    p = 20;
    l >>= 20;
  }
  if (l & 0xFFC00) {
    p += 10;
    l >>= 10;
  }
  p += log2_tab[l];
  return p;
}

